<?php
$x = $_GET["radius"];
$y = $_GET["visota"];
if ((preg_match('/^(\d)+(\.(\d)+)*$/', $x))&&(preg_match('/^(\d)+(\.(\d)+)*$/', $y)))
{
	$rad = $x * 1.0;
	$vis = $y * 1.0;
	if (($rad != 0)&&($vis != 0))
	{
		echo "Объем шарового сектора с радиусом ",$rad," и высотой ",$vis," равен ", 2 / 3 * 3.14 * $rad * $rad * $vis;
	}
	else
	{
		echo "Радиус и высота не должны быть равны 0.";
	}
}
else
{
	echo "Неправильный формат числа.",$x,$y;
}
?>